		SELECT *, 
	       MATCH (PRODUCT_NAME, PRODUCT_BRAND) AGAINST (vSearchInput IN BOOLEAN MODE) AS RELEVANCE
		   FROM PRODUCTS WHERE ID_STORE = nIdStore AND ACTIVE = 1 HAVING RELEVANCE > 0 ORDER BY RELEVANCE DESC LIMIT 20
	) P;
